#include <iostream>
int main()
{
    int N;
    std::cin >> N;
    int ans = 0;
    for (int i = 1; i <= 100; i++) {
        if (i % N != 0) {
            ans++;
        }
    }
    std::cout << ans << std::endl;
    return 0;
}
