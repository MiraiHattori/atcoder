#include <iostream>
int main()
{
    int n;
    std::cin >> n;
    if (n <= 111) {
        std::cout << 111 << std::endl;
    } else if (n <= 222) {
        std::cout << 222 << std::endl;
    } else if (n <= 333) {
        std::cout << 333 << std::endl;
    } else if (n <= 444) {
        std::cout << 444 << std::endl;
    } else if (n <= 555) {
        std::cout << 555 << std::endl;
    } else if (n <= 666) {
        std::cout << 666 << std::endl;
    } else if (n <= 777) {
        std::cout << 777 << std::endl;
    } else if (n <= 888) {
        std::cout << 888 << std::endl;
    } else if (n <= 999) {
        std::cout << 999 << std::endl;
    }
    return 0;
}
